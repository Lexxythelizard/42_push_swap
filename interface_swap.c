void	sa(t_interface	*stacks)
{
	s(stacks -> a);
}

void	sb(t_interface	*stacks)
{
	s(stacks -> b);
}

void	ss(t_interface	*stacks)
{
	s(stacks -> a);
	s(stacks -> b);
}
